    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        try {
            if(tfd_Serie.getText().equals("")){
            lbl_error.setVisible(true);
            lbl_error.setText("Por favor ingrese una serie");
        }else{
            lbl_error.setVisible(false);
            searchCar(Integer.parseInt(tfd_Serie.getText()));
            }
        } catch (IOException e) {
            System.err.println("The vehicle can't be in the file");
        }
    }//GEN-LAST:event_btn_searchActionPerformed

    private void btn_UpDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_UpDateActionPerformed
        // TODO add your handling code here
        
        if(tfd_Serie.getText().equals("")){
            lbl_error.setVisible(true);
            lbl_error.setText("Por favor ingrese una serie");
        }else{
            lbl_error.setVisible(false);
        int series = Integer.parseInt(tfd_Serie.getText());
        String name =tfd_Name.getText();
        int Age = Integer.parseInt(tfd_age.getText());
        boolean america= Boolean.parseBoolean(cb_american.getSelectedItem().toString());
        float Kilo = 0;
        try {
            File file = new File("./vehicle.dat");
            VehicleFile vehicleFile = new VehicleFile(file);
            Vehicle car = new Vehicle();
        ArrayList<Vehicle> myArrayList = vehicleFile.getVehicleList();
        for(int i=0;i<myArrayList.size();i++){
            car = myArrayList.get(i);
            if(car.getSeries()==series){
               Kilo=car.getMileage();
            }
        }
        Vehicle carNew = new Vehicle(name, Age, Kilo, america, series);
        vehicleFile.updateCar(carNew, series);
        vehicleFile.cerrar();
        } catch (IOException ex) {
            System.err.println("The vehicle can't be in the file");
        }
        tfd_Name.setText("");
        tfd_Serie.setText("");
        tfd_age.setText("");
        tfd_Serie.requestFocus();
        }
    }//GEN-LAST:event_btn_UpDateActionPerformed

