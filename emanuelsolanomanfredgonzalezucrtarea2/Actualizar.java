    //method to search the single car
    public void searchCar(int serie) throws IOException{
        
        File file = new File("./vehicle.dat");
        VehicleFile vehicleFile = new VehicleFile(file);
        Vehicle car = new Vehicle();
        ArrayList<Vehicle> myArrayList = vehicleFile.getVehicleList();
        
        for(int i=0;i<myArrayList.size();i++){
            vehicleFile.cerrar();
            car = myArrayList.get(i);
            if(car.getSeries()==serie){
                vehicleFile.cerrar();
                tfd_Name.setText(car.getName());
                tfd_age.setText(""+car.getAge());
                cb_american.setSelectedItem(Boolean.toString(car.isAmerican()));
                lbl_error.setVisible(false);
                
            }else{
                lbl_error.setText("La serie ingresada no corresponde a ningún automovil");
                lbl_error.setVisible(true);
                
            }
        }
        
        vehicleFile.cerrar();
        
    }
    
